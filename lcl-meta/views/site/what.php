<big><big>There are many types of evidence,<br>
but not all are equally reliable.<br>
<br>
<span style="font-weight: bold;">Systematic reviews and meta-analyses* </span><br>
are considered to be the least biased forms of evidence synthesis.<br>
<br>
This is true, if they follow a systematic review process &#8211; <br>
procedures designed to reduce risk of bias* <br>
and increase transparency*.<br>
<br>
To reduce risk of bias in our database <br>
we followed the systematic review process. &nbsp;<br>
We are also transparent about what we did &#8211; <br>
technical report on search and data extraction<br>
